namespace NetEasePlayer_UWP
{
    /// <summary>
    /// 可用于自身或导航至 Frame 内部的空白页。
    /// </summary>
    public sealed partial class PlayerPage : Page
    {
        public PlayerPage()
        {
            this.InitializeComponent();
        }
         protected override void OnNavigatedTo(NavigationEventArgs e)
        {
            base.OnNavigatedTo(e);
            //这个e.Parameter是获取传递过来的参数，其实大家应该再次之前判断这个参数是否为null的，我偷懒了
            Live live = (Live)e.Parameter;
            Play(live.Url);
            //MessageDialog msg = new MessageDialog("开始播放"+live.Name);
            //await msg.ShowAsync();
        }
        private void Play(String url)
        {
            video_player.Source = MediaSource.CreateFromUri( new Uri(url) );
            video_player.Visibility = Visibility.Visible;
        }

    }
}
